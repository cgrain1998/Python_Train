# if 语句

age = 16
if age >= 16:
    print("你已经大于16岁")

# if ... else...
print("\n if ...else...")

if age > 16:
    print("大于16")
else:
    print("你小于16")

# if ..elif  ...else
print("#if ..elif  ...else")
if age > 16:
    print(age)
elif age == 16:
    print("等于16")
else:
    print("小于16")
